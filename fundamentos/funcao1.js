
// Definindo uma função chamada 'soma' que aceita dois parâmetros 'a' e 'b'
function soma(a, b) {
    const resultado = a + b;
    return resultado; // Retornando a soma dos parâmetros
}

// Chamando a função 'soma' e armazenando o resultado na variável 'resultadoSoma'
const resultadoSoma = soma(5, 3);

console.log(resultadoSoma); // Isso exibirá '8' no console